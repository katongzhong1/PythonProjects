# Python 的命名规范 [官方命名规范]http://wiki.jiayun.org/PEP_8_--_Style_Guide_for_Python_Code#.E5.91.BD.E5.90.8D.E6.85.A3.E4.BE.8B
# ==> 1. 文件名: 全小写, 可使用下划线
# ==> 2. 函数名: 小写, 可以用下划线风格单词以正佳可读性
# ==> 3. 函数的参数: 如果一个函数的参数名称和保留的关键字冲突, 通常使用一个后缀下划线好于使用缩写或奇怪的拼写
# ==> 4. 变量: 变量名全部小写, 由下划线连接各个单词