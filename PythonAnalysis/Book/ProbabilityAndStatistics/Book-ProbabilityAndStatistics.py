# 一: 术语
#     a. 汇总统计量(summary statistic)
#        ==> 通过计算将一个数据集归结到一个数字(或者是少量的几个数字), 而这个数字能表示数据的某些特点。
#     b. 重编码(recode)
#        ==> 通过对原始数据进行计算或是其他逻辑处理得到的值
#     c. 横断面研究(cross-sectional study)
#        ==> 收集群体在特定时间点的数据的研究
#     d. 纵贯研究(longitudinal study)
#        ==> 跟踪群体, 随着时间推移对同一组人反复采集数据的研究
#     e. 统计显著性(statistically significant)
#        ==> 若一个直观效应不太可能是由随机因素引起的, 就是统计显著的
#     f. 直观效应(apparent effect)
#        ==> 表示发生了某种有意思的事情的度量或汇总统计量
