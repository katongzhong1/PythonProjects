# 一:
#   1. 均值(mean)和平均值(average)
#       a.定义
#         ==> 均值: 如果一个包含n个值的样本xi, 那么它们的均值μ就等于这些值的总和除以值的数量
#       b.区别
#         ==> 1. 样本的均值是根据公司计算出来的一个汇总统计量;
#         ==> 2. 平均值是若干种可以用于描述样本的典型值或集中趋势的汇总统计量之一。
#   2. 方差和标准差
#      ==> 均值描述集中趋势, 方差描述分散情况
#   3. 分布
#      ==> a. 分布描述了各个值出现的频繁程度
#      ==> b. 表示分布的最常用的方法是直方图(histogram), 用于展示各个值出现的频数或概率
#      ==> c. 计算频数, 通过如下方法, 得到一个将值映射到其频数的字典
#          hist = {}
#          for x in t:
#            hist[x] = hist.get(x, 0) + 1
#      ==> d. 归一化(normalizaiton): 将如上字典除以n即可把频数转换成概率
#          n = float(len(t))
#          pmf = {}
#          for x, freq in hist.items():
#            pmf[x] = freq / n
#      ==> e. 归一化后的直方图称为 PMF (Probability Mass Function, 概率质量函数)
#   4. 直方图
